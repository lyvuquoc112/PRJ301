package pe.model;

/**
 *
 * @author hanly
 */
public class UpdateError {

    private String nameError;
    private String descriptionError;
    private String priceError;
    private String sizeError;
    private String statusError;

    public UpdateError() {
    }

    /**
     * @return the nameError
     */
    public String getNameError() {
        return nameError;
    }

    /**
     * @param nameError the nameError to set
     */
    public void setNameError(String nameError) {
        this.nameError = nameError;
    }

    /**
     * @return the descriptionError
     */
    public String getDescriptionError() {
        return descriptionError;
    }

    /**
     * @param descriptionError the descriptionError to set
     */
    public void setDescriptionError(String descriptionError) {
        this.descriptionError = descriptionError;
    }

    /**
     * @return the priceError
     */
    public String getPriceError() {
        return priceError;
    }

    /**
     * @param priceError the priceError to set
     */
    public void setPriceError(String priceError) {
        this.priceError = priceError;
    }

    /**
     * @return the sizeError
     */
    public String getSizeError() {
        return sizeError;
    }

    /**
     * @param sizeError the sizeError to set
     */
    public void setSizeError(String sizeError) {
        this.sizeError = sizeError;
    }

    /**
     * @return the statusError
     */
    public String getStatusError() {
        return statusError;
    }

    /**
     * @param statusError the statusError to set
     */
    public void setStatusError(String statusError) {
        this.statusError = statusError;
    }
    
    
}
